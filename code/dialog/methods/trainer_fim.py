    def prediction_step(
            self,
            model: nn.Module,
            inputs: Dict[str, Union[torch.Tensor, Any]],
            prediction_loss_only: bool,
            ignore_keys: Optional[List[str]] = None,
    ) -> Tuple[Optional[float], Optional[torch.Tensor], Optional[torch.Tensor]]:
        """
        Perform an evaluation step on :obj:`model` using obj:`inputs`.

        Subclass and override to inject custom behavior.

        Args:
            model (:obj:`nn.Module`):
                The model to evaluate.
            inputs (:obj:`Dict[str, Union[torch.Tensor, Any]]`):
                The inputs and targets of the model.

                The dictionary will be unpacked before being fed to the model. Most models expect the targets under the
                argument :obj:`labels`. Check your model's documentation for all accepted arguments.
            prediction_loss_only (:obj:`bool`):
                Whether or not to return the loss only.

        Return:
            Tuple[Optional[float], Optional[torch.Tensor], Optional[torch.Tensor]]: A tuple with the loss, logits and
            labels (each being optional).
        """
        self.args.predict_with_generate = True
        if not self.args.predict_with_generate or prediction_loss_only:
            return super().prediction_step(
                model, inputs, prediction_loss_only=prediction_loss_only, ignore_keys=ignore_keys
            )

        has_labels = "labels" in inputs
        inputs = self._prepare_inputs(inputs)

        # XXX: adapt synced_gpus for fairscale as well
        gen_kwargs = {
            "max_length": self._max_length if self._max_length is not None else self.model.config.max_length,
            "num_beams": self._num_beams if self._num_beams is not None else self.model.config.num_beams,
            "synced_gpus": True if is_deepspeed_zero3_enabled() else False,
        }

        if hasattr(self, "num_return_sequences"):
            gen_kwargs["num_return_sequences"]: self._num_return_sequences if self._num_return_sequences is not None else 1

        if any(["GPT" in architecture for architecture in self.model.config.architectures]):
            # input_ids will also be part of the output for encoder-only models
            gen_kwargs["max_length"] += inputs["input_ids"].shape[-1]

        generation_output = self.model.generate(
            inputs["input_ids"],
            attention_mask=inputs["attention_mask"],
            output_scores=True,
            return_dict_in_generate=True,
            **gen_kwargs,
        )
        generated_tokens = generation_output.sequences
        sequence_scores = generation_output.sequences_scores

        # in case the batch is shorter than max length, the output should be padded
        if generated_tokens.shape[-1] < gen_kwargs["max_length"]:
            generated_tokens = self._pad_tensors_to_max_len(
                generated_tokens, gen_kwargs["max_length"])

        with torch.no_grad():
            # labels should be defined in any case
            if any(["T5" in architecture for architecture in model.config.architectures]) and "decoder_input_ids" not in inputs and not has_labels:
                inputs["decoder_input_ids"] = torch.full((inputs["input_ids"].shape[0], 1), model.config.pad_token_id).to(inputs["input_ids"].device)
            outputs = model(**inputs)
            if has_labels:
                if self.label_smoother is not None:
                    loss = self.label_smoother(
                        outputs, inputs["labels"]).mean().detach()
                else:
                    loss = (outputs["loss"] if isinstance(
                        outputs, dict) else outputs[0]).mean().detach()
            else:
                loss = None

        if self.args.prediction_loss_only:
            return (loss, None, None)

        if has_labels:
            labels = inputs["labels"]
            if labels.shape[-1] < gen_kwargs["max_length"]:
                labels = self._pad_tensors_to_max_len(labels, gen_kwargs["max_length"])

        return (loss, generated_tokens, labels if has_labels else None, sequence_scores)

    def _pad_tensors_to_max_len(self, tensor, max_length):
        if self.tokenizer is None:
            raise ValueError(
                f"Tensor need to be padded to `max_length={max_length}` but no tokenizer was passed when creating "
                "this `Trainer`. Make sure to create your `Trainer` with the appropriate tokenizer."
            )
        # If PAD token is not defined at least EOS token has to be defined
        pad_token_id = (
            self.tokenizer.pad_token_id if self.tokenizer.pad_token_id is not None else self.tokenizer.eos_token_id
        )

        padded_tensor = pad_token_id * torch.ones(
            (tensor.shape[0],
             max_length), dtype=tensor.dtype, device=tensor.device
        )
        padded_tensor[:, : tensor.shape[-1]] = tensor
        return padded_tensor


    def evaluation_loop(
        self,
        dataloader: DataLoader,
        description: str,
        prediction_loss_only: Optional[bool] = None,
        ignore_keys: Optional[List[str]] = None,
        metric_key_prefix: str = "eval",
        test_dataset=None,
    ) -> EvalLoopOutput:
        """
        Prediction/evaluation loop, shared by `Trainer.evaluate()` and `Trainer.predict()`.

        Works both with or without labels.
        """
        args = self.args

        prediction_loss_only = prediction_loss_only if prediction_loss_only is not None else args.prediction_loss_only

        # if eval is called w/o train init deepspeed here
        if args.deepspeed and not self.deepspeed:

            # XXX: eval doesn't have `resume_from_checkpoint` arg but we should be able to do eval
            # from the checkpoint eventually
            deepspeed_engine, _, _ = deepspeed_init(
                self, num_training_steps=0, resume_from_checkpoint=None, inference=True
            )
            self.model = deepspeed_engine.module
            self.model_wrapped = deepspeed_engine
            self.deepspeed = deepspeed_engine

        model = self._wrap_model(self.model, training=False, dataloader=dataloader)

        # if full fp16 or bf16 eval is wanted and this ``evaluation`` or ``predict`` isn't called
        # while ``train`` is running, cast it to the right dtype first and then put on device
        if not self.is_in_train:
            if args.fp16_full_eval:
                model = model.to(dtype=torch.float16, device=args.device)
            elif args.bf16_full_eval:
                model = model.to(dtype=torch.bfloat16, device=args.device)

        batch_size = self.args.eval_batch_size

        logger.info(f"***** Running {description} *****")
        if has_length(dataloader):
            logger.info(f"  Num examples = {self.num_examples(dataloader)}")
        else:
            logger.info("  Num examples: Unknown")
        logger.info(f"  Batch size = {batch_size}")

        model.eval()

        self.callback_handler.eval_dataloader = dataloader
        # Do this before wrapping.
        eval_dataset = getattr(dataloader, "dataset", None)

        if is_torch_tpu_available():
            dataloader = pl.ParallelLoader(dataloader, [args.device]).per_device_loader(args.device)

        if args.past_index >= 0:
            self._past = None

        # Initialize containers
        # losses/preds/labels on GPU/TPU (accumulated for eval_accumulation_steps)
        losses_host = None
        preds_host = None
        labels_host = None
        inputs_host = None
        scores_host = None

        # losses/preds/labels on CPU (final containers)
        all_losses = None
        all_preds = None
        all_labels = None
        all_inputs = None
        all_scores = None

        fim = self.fim_loop(dataloader, test_dataset)

        return EvalLoopOutput(predictions=torch.tensor([[]]*len(dataloader)), label_ids=torch.tensor([[]]*len(dataloader)), metrics={}, num_samples=0, scores={})

    def _maybe_log_save_evaluate(self, tr_loss, model, trial, epoch, ignore_keys_for_eval):
        if self.control.should_log:
            if is_torch_tpu_available():
                xm.mark_step()

            logs: Dict[str, float] = {}

            # all_gather + mean() to get average loss over all processes
            tr_loss_scalar = self._nested_gather(tr_loss).mean().item()

            # reset tr_loss to zero
            tr_loss -= tr_loss

            logs["loss"] = round(tr_loss_scalar / (self.state.global_step - self._globalstep_last_logged), 4)
            logs["learning_rate"] = self._get_learning_rate()

            self._total_loss_scalar += tr_loss_scalar
            self._globalstep_last_logged = self.state.global_step
            self.store_flos()

            self.log(logs)

        metrics = None
        fim = None
        if self.control.should_evaluate:
            metrics = self.evaluate(ignore_keys=ignore_keys_for_eval)
            self._report_to_hp_search(trial, epoch, metrics)
            fim = self.fim_loop()
        val_metric = None
        if self.control.should_save:
            val_metric = self._save_checkpoint(model, trial, metrics=metrics)
            self.control = self.callback_handler.on_save(self.args, self.state, self.control)
        return fim, val_metric

    def fim_loop(self, dataloader, test_dataset):
        """
        Function to calculate the trace of Fisher Information
        """
        model = self.model.to('cuda:0')
        precision_matrices = {}
        named_parameters = dict(model.named_parameters())
        for n, p in named_parameters.items():
            logger.info(f"Named parameters: {n}")
            precision_matrices[n] = variable(torch.zeros_like(p.data))
        model.eval()
        for step, inputs in enumerate(dataloader):
            model.zero_grad()
            inputs = self._prepare_inputs(inputs)
            if any(["T5" in architecture for architecture in model.config.architectures]) and "decoder_input_ids" not in inputs and "labels" not in inputs:
                labels = self.tokenizer(test_dataset[step*inputs["input_ids"].shape[0]: step*inputs["input_ids"].shape[0]+inputs["input_ids"].shape[0]]["response"], return_tensors="pt", max_length=128, padding=True)["input_ids"].to("cuda:0")
                inputs["labels"] = labels
            outputs = model(**inputs)
            try:
                output = outputs.logits
            except:
                output = torch.cat([outputs.start_logits, outputs.end_logits], dim=0)

            log_probs = F.log_softmax(output, dim=-1)

            # random sampling of output labels
            cumsums = torch.logcumsumexp(log_probs, dim=-1).exp()
            outdx = torch.searchsorted(cumsums, torch.rand(tuple(list(cumsums.shape[:-1]) + [1])).to(cumsums.device))
            outdx = outdx.squeeze(-1)

            loss = F.nll_loss(log_probs.view(-1, log_probs.size(-1)), outdx.view(-1))
            loss.backward()
            for n, p in named_parameters.items():
                if p.requires_grad and p.grad is not None:
                    precision_matrices[n].data += p.grad.data ** 2 / output.shape[0]

        precision_matrices = {n: (p / (step+1.0)).detach() for n, p in precision_matrices.items()}
        for n, p in named_parameters.items():
            named_parameters[n].data.copy_(precision_matrices[n].data)
        for n, p in precision_matrices.items():
            named_parameters = dict(model.named_parameters())

        output_dir = os.path.join(self.args.output_dir, "fisher_estimates")
        self.save_model(output_dir)
        return None
